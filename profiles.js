// Helper: get profiles for a type
function getProfilesForType(type) {
  return PROFILES[type] ? PROFILES[type].profiles : [];
}

// Helper: get a specific profile
function getProfile(type, name) {
  const list = getProfilesForType(type);
  return list.find(p => p.name === name) || null;
}

